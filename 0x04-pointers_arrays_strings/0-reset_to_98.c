#include "holberton.h"
/**
 * reset_to_98 - Takes a pointer to an int as parameter to update value to 98.
 * @n: a variable declared as n integer.
 * If size is less than 0, function to print a \n
 *
 * Description: Updates value of *n.
 * Return: Always 0.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
